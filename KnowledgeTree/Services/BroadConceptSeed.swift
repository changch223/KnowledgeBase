//  概念階層抽出 (generateConceptHierarchy) の prompt に、その記事カテゴリの代表的な
//  広い概念をヒントとして渡す。AI はこのシードから最も合うものを選び、無ければ簡潔に命名できる。
//  シードは「強制」ではなく「ヒント」: 表記揺れ・新概念は agent loop (spec 076) が後で正規化する。
//
//  例: テクノロジー記事 → シード [生成AI/LLM, データエンジニアリング, ...] を提示
//     → Text-to-SQL 記事の広い概念に「生成AI / LLM」が選ばれる。
//

import Foundation

enum BroadConceptSeed {
    /// カテゴリ名 → 代表的な広い概念のシード配列。
    /// カテゴリは CategorySeed の 10 種に対応 (動的カテゴリには seed なし = AI 自由命名)。
    static let byCategory: [String: [String]] = [
        "テクノロジー": [
            "生成AI / LLM", "機械学習", "データエンジニアリング", "ソフトウェア開発",
            "Web開発", "クラウド", "セキュリティ", "プログラミング言語", "ハードウェア / ガジェット",
        ],
        "経済": [
            "マクロ経済", "金融市場", "スタートアップ / 起業", "マーケティング",
            "企業経営", "プロダクトマネジメント", "暗号資産",
        ],
        "健康": [
            "栄養 / 食事", "メンタルヘルス", "運動 / フィットネス", "睡眠", "医療 / 病気",
        ],
        "デザイン": [
            "UI / UX", "グラフィックデザイン", "プロダクトデザイン", "建築 / 空間",
        ],
        "学術": [
            "数学", "物理学", "生物学", "計算機科学", "人文 / 社会科学", "研究手法",
        ],
        "アート": [
            "現代美術", "音楽", "文学", "映像 / 写真",
        ],
        "ニュース": [
            "政治", "国際情勢", "社会 / 事件", "災害 / 防災", "法 / 制度",
        ],
        "スポーツ": [
            "サッカー", "野球", "バスケットボール", "陸上 / 競技", "格闘技",
        ],
        "エンタメ": [
            "映画", "テレビ / 配信", "ゲーム", "アニメ / マンガ", "芸能",
        ],
        "その他": [],
    ]

    /// 指定カテゴリの広域概念シードを " / " 区切りで返す (prompt 埋め込み用)。
    /// シードが無い (動的カテゴリ / その他) 場合は空文字 = AI 自由命名。
    static func promptHint(for categoryName: String) -> String {
        let seeds = byCategory[categoryName] ?? []
        return seeds.joined(separator: " / ")
    }
}
